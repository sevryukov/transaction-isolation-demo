USE IsoLocksDemo;
GO

/* ------------------------------------------------------------------------------
   СЦЕНАРИЙ 1: ГРЯЗНОЕ ЧТЕНИЕ (DIRTY READ)
------------------------------------------------------------------------------ */

-- ШАГ 2: Пытаемся прочитать данные (дефолтный READ COMMITTED).
-- Запрос ЗАВИСНЕТ! Почему? Нам нужна S-блокировка для чтения, 
-- но Окно 1 держит X-блокировку (меняет данные). S и X несовместимы.
-- Выделите, выполните, подождите пару секунд, затем нажмите красный квадрат STOP (Cancel).
SELECT * FROM Accounts WHERE ID = 1; 

-- ШАГ 3: Игнорируем блокировки! Включаем READ UNCOMMITTED (или хинт WITH (NOLOCK)).
-- Это позволяет читать данные без запроса S-блокировок.
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED;
SELECT * FROM Accounts WHERE ID = 1; -- Ура! Мы прочитали 1500.00. 
-- Но Окно 1 еще не сделало Commit. Мы прочитали "Грязные данные".

-- >>> ПЕРЕЙДИТЕ В ОКНО 1 (ШАГ 4) <<<

-- ШАГ 5: Снова читаем. Баланс вернулся к 1000.00. 
-- Данные 1500.00, на основе которых мы могли уже построить отчет, оказались иллюзией.
SELECT * FROM Accounts WHERE ID = 1; 

-- >>> ПЕРЕЙДИТЕ В ОКНО 1 (ШАГ 6) <<<


/* ------------------------------------------------------------------------------
   СЦЕНАРИЙ 2: НЕПОВТОРЯЮЩЕЕСЯ ЧТЕНИЕ (READ COMMITTED vs REPEATABLE READ)
------------------------------------------------------------------------------ */

-- ШАГ 7: Окно 1 находится в READ COMMITTED. 
-- Оно прочитало строку и СРАЗУ сбросило S-блокировку. 
-- Поэтому наш UPDATE проходит мгновенно. Мы портим им согласованность.
UPDATE Accounts SET Balance = 700.00 WHERE ID = 2;

-- >>> ПЕРЕЙДИТЕ В ОКНО 1 (ШАГ 8 и 9) <<<

-- ШАГ 10: Теперь Окно 1 перешло в REPEATABLE READ. 
-- Оно держит S-блокировку "мертвой хваткой" до конца транзакции.
-- Наш UPDATE требует X-блокировку. X и S несовместимы.
-- Запрос ЗАВИСАЕТ (ждет снятия S-блокировки). Оставьте его висеть и переходите в Окно 1.
UPDATE Accounts SET Balance = 900.00 WHERE ID = 2;

-- >>> ПЕРЕЙДИТЕ В ОКНО 1 (ШАГ 11) <<<


/* ------------------------------------------------------------------------------
   СЦЕНАРИЙ 3: ФАНТОМНОЕ ЧТЕНИЕ (REPEATABLE READ vs SERIALIZABLE)
------------------------------------------------------------------------------ */

-- ШАГ 13: Окно 1 (в REPEATABLE READ) заблокировало только СУЩЕСТВУЮЩИЕ строки диапазона.
-- Мы свободно вставляем новую строку рядом. Запрос проходит мгновенно.
INSERT INTO Accounts (ID, AccountName, Balance) VALUES (4, 'Dave', 800.00);

-- >>> ПЕРЕЙДИТЕ В ОКНО 1 (ШАГ 14 и 15) <<<

-- ШАГ 16: Окно 1 включило SERIALIZABLE. 
-- Оно наложило RangeS-S блокировку на ВЕСЬ диапазон (Balance >= 500.00).
-- Мы пытаемся вставить Dave с балансом 800. Эта цифра попадает в заблокированный диапазон!
-- Запрос ЗАВИСАЕТ. Оставьте висеть.
INSERT INTO Accounts (ID, AccountName, Balance) VALUES (4, 'Dave', 800.00);

-- >>> ПЕРЕЙДИТЕ В ОКНО 1 (ШАГ 17) <<<


/* ------------------------------------------------------------------------------
   СЦЕНАРИЙ 4: SNAPSHOT (Оптимистичная блокировка через TempDB)
------------------------------------------------------------------------------ */

-- ШАГ 19: Окно 1 читает строку в режиме SNAPSHOT.
-- Мы (писатели) обновляем эту же строку. 
-- Блокировки нет! Мы накладываем X-блокировку, и нам никто не мешает (нет S-блокировок).
UPDATE Accounts SET Balance = 3000.00 WHERE ID = 1;

-- Убедимся, что мы данные изменили:
SELECT * FROM Accounts WHERE ID = 1; -- Видим 3000

-- >>> ПЕРЕЙДИТЕ В ОКНО 1 (ШАГ 20) <<<


/* ------------------------------------------------------------------------------
   СЦЕНАРИЙ 5: ВЗАИМОБЛОКИРОВКА (DEADLOCK)
------------------------------------------------------------------------------ */

-- ШАГ 22: Окно 1 уже заблокировало Alice (ID=1). 
-- Мы накладываем X-блокировку на Bob (ID=2). Успешно.
SET TRANSACTION ISOLATION LEVEL READ COMMITTED;
BEGIN TRAN;
UPDATE Accounts SET Balance = Balance + 10 WHERE ID = 2;

-- ШАГ 23: Мы пытаемся обновить Alice. Но она заблокирована Окном 1.
-- Наш запрос ЗАВИСАЕТ (Окно 2 ждет Окно 1). Оставьте его висеть.
UPDATE Accounts SET Balance = Balance + 10 WHERE ID = 1;

-- >>> ПЕРЕЙДИТЕ В ОКНО 1 (ШАГ 24) <<<

-- ШАГ 26: Откат. Если Окно 2 выжило после Deadlock (или транзакция осталась открытой),
-- откатите ее, чтобы не мусорить в базе.
ROLLBACK TRAN;