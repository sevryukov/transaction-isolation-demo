/* =========================================================================================
   КУРС: Оптимизация БД. MS SQL Server 2022
   ТЕМА: Уровни изоляции транзакций и блокировки (СКРИПТ 1 - ОКНО 1)
   
   ТЕОРИЯ: БЛОКИРОВКИ (LOCKS)
   Блокировки — это механизм СУБД для обеспечения согласованности данных при их 
   одновременном изменении/чтении разными пользователями.
   Базовые типы:
   - (S) Shared (Разделяемая): Накладывается при ЧТЕНИИ. Несколько транзакций могут 
     одновременно держать S-блокировку на одну строку (читать вместе можно).
   - (X) Exclusive (Эксклюзивная): Накладывается при ИЗМЕНЕНИИ (INSERT/UPDATE/DELETE). 
     Несовместима ни с какими другими. Если кто-то меняет данные (X), другие не могут 
     их ни читать (S), ни менять (X), пока транзакция не завершится.
========================================================================================= */

-- ШАГ 0: ПОДГОТОВКА БД (Выполнить один раз перед началом)
USE master;
GO
IF DB_ID('IsoLocksDemo') IS NOT NULL 
