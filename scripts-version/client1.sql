    ALTER DATABASE IsoLocksDemo SET SINGLE_USER WITH ROLLBACK IMMEDIATE;
    DROP DATABASE IsoLocksDemo;
END
CREATE DATABASE IsoLocksDemo;
GO
ALTER DATABASE IsoLocksDemo SET ALLOW_SNAPSHOT_ISOLATION ON; -- Включаем версионирование для Сценария 4
GO
USE IsoLocksDemo;
