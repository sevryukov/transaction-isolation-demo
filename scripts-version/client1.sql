CREATE TABLE Accounts (
    ID INT PRIMARY KEY, AccountName NVARCHAR(50), Balance DECIMAL(10,2)
);
GO
INSERT INTO Accounts (ID, AccountName, Balance) 
VALUES (1, 'Alice', 1000.00), (2, 'Bob', 500.00), (3, 'Charlie', 200.00);
GO


/* =========================================================================================
   СЦЕНАРИЙ 1: ГРЯЗНОЕ ЧТЕНИЕ (DIRTY READ)
   
   Теория: Чтение данных, которые были изменены другой транзакцией, но еще не зафиксированы (COMMIT).
   Если та транзакция сделает ROLLBACK, мы будем работать с данными, которых "никогда не было".
   
   Кейс из практики: Формирование тяжелого аналитического отчета или дашборда с примерными
   суммами за день. Нам не важна точность до копейки, но нам важно, чтобы отчет строился
   быстро и не блокировал работу операционистов (писателей). В этом случае используют NOLOCK.
========================================================================================= */

-- ШАГ 1: Начинаем транзакцию, меняем данные, НЕ фиксируем
BEGIN TRAN;
UPDATE Accounts SET Balance = 1500.00 WHERE ID = 1;

-- Посмотрим системное представление sys.dm_tran_locks.
-- Мы увидим блокировку с request_mode = 'X' (Exclusive) на строке (KEY).
-- Она говорит: "Я меняю эти данные, никто другой не имеет к ним доступа!"
SELECT resource_type, request_mode, request_status 
FROM sys.dm_tran_locks WHERE request_session_id = @@SPID AND resource_type NOT IN ('DATABASE', 'METADATA');

-- >>> ПЕРЕЙДИТЕ В ОКНО 2 (ШАГ 2 и 3) <<<

-- ШАГ 4: Откатываем изменения. Данных (1500.00) в базе не осталось, 
-- но Окно 2 их уже успело прочитать и, возможно, отправить клиенту!
ROLLBACK TRAN;

-- >>> ПЕРЕЙДИТЕ В ОКНО 2 (ШАГ 5) <<<


/* =========================================================================================
   СЦЕНАРИЙ 2: НЕПОВТОРЯЮЩЕЕСЯ ЧТЕНИЕ (READ COMMITTED vs REPEATABLE READ)
   
   Теория: Внутри одной транзакции мы дважды читаем одну и ту же строку, но получаем 
   разные результаты, потому что между нашими чтениями кто-то другой ее изменил.
   
   Кейс из практики: Покупка в интернет-магазине.
   1. Читаем остаток товара: 1 шт.
   2. Делаем долгие проверки (валидация карты, скидки).
   3. Кто-то другой покупает этот товар (UPDATE остатка в 0).
   4. Мы пытаемся списать товар, но его уже нет. Баланс уходит в минус.
========================================================================================= */

-- ШАГ 6: Читаем данные в дефолтном READ COMMITTED
SET TRANSACTION ISOLATION LEVEL READ COMMITTED;
BEGIN TRAN;
SELECT * FROM Accounts WHERE ID = 2; -- Видим 500.00

-- Смотрим блокировки: ИХ НЕТ! 
-- В READ COMMITTED разделяемая S-блокировка снимается СРАЗУ ПОСЛЕ того, 
-- как сервер прочитал строку. Она не держится до конца транзакции.
SELECT resource_type, request_mode FROM sys.dm_tran_locks 
WHERE request_session_id = @@SPID AND resource_type NOT IN ('DATABASE', 'METADATA');

-- >>> ПЕРЕЙДИТЕ В ОКНО 2 (ШАГ 7) <<<

-- ШАГ 8: Снова читаем. Данные изменились на 700! (Неповторяющееся чтение). Ошибка логики.
SELECT * FROM Accounts WHERE ID = 2; 
ROLLBACK TRAN;

-- ШАГ 9: ИСПРАВЛЕНИЕ. Включаем REPEATABLE READ
SET TRANSACTION ISOLATION LEVEL REPEATABLE READ;
BEGIN TRAN;
SELECT * FROM Accounts WHERE ID = 2; -- Видим 700.00

-- Теперь Shared (S) блокировка удерживается до COMMIT/ROLLBACK!
-- Она защищает строку: "Я читаю эти данные, не смейте их менять!"
SELECT resource_type, request_mode FROM sys.dm_tran_locks 
WHERE request_session_id = @@SPID AND resource_type NOT IN ('DATABASE', 'METADATA');

-- >>> ПЕРЕЙДИТЕ В ОКНО 2 (ШАГ 10) <<<

-- ШАГ 11: Завершаем транзакцию. S-блокировка снимается. После этого Окно 2 "отвиснет".
COMMIT TRAN;


/* =========================================================================================
   СЦЕНАРИЙ 3: ФАНТОМНОЕ ЧТЕНИЕ (REPEATABLE READ vs SERIALIZABLE)
   
   Теория: REPEATABLE READ защищает существующие строки от изменения. Но он НЕ защищает 
   от появления НОВЫХ строк, подходящих под условие (WHERE). Нельзя поставить блокировку 
   на строку, которой еще не существует!
   
   Кейс из практики: Бронирование переговорки / отеля на даты.
   1. Транзакция А проверяет: "Есть ли брони на 15 мая?" (SELECT... WHERE Date='15.05').
   2. Видит, что пусто. Решает бронировать.
   3. Транзакция Б в это же мгновение делает INSERT брони на 15 мая (фантом).
   4. Транзакция А делает свой INSERT. Итог: двойное бронирование (Overbooking).
========================================================================================= */

-- Восстановим данные перед тестом
UPDATE Accounts SET Balance = 500.00 WHERE ID = 2; DELETE FROM Accounts WHERE ID = 4;

-- ШАГ 12: Читаем диапазон строк в REPEATABLE READ
SET TRANSACTION ISOLATION LEVEL REPEATABLE READ;
BEGIN TRAN;
SELECT * FROM Accounts WHERE Balance >= 500.00; -- Видим Alice(1000) и Bob(500)

-- >>> ПЕРЕЙДИТЕ В ОКНО 2 (ШАГ 13) <<<

-- ШАГ 14: Снова читаем. Появился "Фантом" Dave (800).
SELECT * FROM Accounts WHERE Balance >= 500.00; 
ROLLBACK TRAN;

-- ШАГ 15: ИСПРАВЛЕНИЕ. Удаляем фантома и включаем абсолютную изоляцию SERIALIZABLE
DELETE FROM Accounts WHERE ID = 4;

SET TRANSACTION ISOLATION LEVEL SERIALIZABLE;
BEGIN TRAN;
SELECT * FROM Accounts WHERE Balance >= 500.00;

-- Видим блокировку диапазонов ключей RangeS-S (Range Shared-Shared).
-- SQL Server блокирует не только сами строки, но и "дырки" (промежутки) в индексе 
-- между значениями, чтобы никто не смог вставить туда новые ключи!
SELECT resource_type, request_mode FROM sys.dm_tran_locks 
WHERE request_session_id = @@SPID AND resource_type NOT IN ('DATABASE', 'METADATA');

-- >>> ПЕРЕЙДИТЕ В ОКНО 2 (ШАГ 16) <<<

-- ШАГ 17: Завершаем транзакцию. Окно 2 выполнит вставку.
COMMIT TRAN;


/* =========================================================================================
   СЦЕНАРИЙ 4: SNAPSHOT (Оптимистичная блокировка через TempDB)
   
   Теория: Главная проблема строгих блокировок — "Читатели блокируют писателей, 
   а писатели читателей". Snapshot решает это через Версионирование строк (Row Versioning).
   При UPDATE старая версия строки копируется в системную БД TempDB.
   Читатели не ставят S-блокировки! Они просто идут в TempDB и читают старую версию.
   
   Кейс из практики: 1С:Предприятие или любая высоконагруженная OLTP-система. Чтобы 
   пользователи, формирующие отчеты, не парализовали работу склада и бухгалтерии.
========================================================================================= */

-- ШАГ 18: Читаем в SNAPSHOT
SET TRANSACTION ISOLATION LEVEL SNAPSHOT;
BEGIN TRAN;
SELECT * FROM Accounts WHERE ID = 1; -- Видим 1000.00

-- >>> ПЕРЕЙДИТЕ В ОКНО 2 (ШАГ 19) <<<

-- ШАГ 20: Снова читаем. Окно 2 уже изменило данные на 3000, 
-- но мы видим СТАРУЮ версию (1000.00) из TempDB! Консистентность транзакции сохранена.
SELECT * FROM Accounts WHERE ID = 1; 
COMMIT TRAN;

-- Вне транзакции теперь видим новые, зафиксированные данные:
SELECT * FROM Accounts WHERE ID = 1;


/* =========================================================================================
   СЦЕНАРИЙ 5: ВЗАИМОБЛОКИРОВКА (DEADLOCK)
   
   Теория: Ситуация, когда Транзакция А ждет ресурс, захваченный Транзакцией Б, 
   а Транзакция Б ждет ресурс, захваченный Транзакцией А.
   Система "зависает" навсегда. СУБД (Deadlock Monitor) раз в 5 секунд ищет такие 
   циклы, выбирает одну транзакцию как "жертву" (victim) и убивает ее (ROLLBACK), 
   чтобы вторая могла продолжить работу. Ошибка 1205.
========================================================================================= */
-- Восстановим баланс
UPDATE Accounts SET Balance = 1000.00 WHERE ID = 1;

-- ШАГ 21: Берем X-блокировку на Alice (ID=1)
SET TRANSACTION ISOLATION LEVEL READ COMMITTED;
BEGIN TRAN;
UPDATE Accounts SET Balance = Balance + 10 WHERE ID = 1;

-- >>> ПЕРЕЙДИТЕ В ОКНО 2 (ШАГ 22 и 23) <<<

-- ШАГ 24: Пытаемся обновить Bob (ID=2). 
-- Он заблокирован Окном 2. Образуется кольцо ожидания! Вызовет Deadlock!
UPDATE Accounts SET Balance = Balance + 10 WHERE ID = 2;

-- ШАГ 25: Очистка (выполните ROLLBACK в выжившей сессии)
ROLLBACK TRAN;